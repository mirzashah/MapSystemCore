#include <MathFunctions.h>

/****************************************************/
double cosDeg(double deg)
/****************************************************/
{
	double rad = deg * (PI/180);
	return (cos(rad));
}

/****************************************************/
double sinDeg(double deg)
/****************************************************/
{
	double rad = deg * (PI/180);
	return (sin(rad));
}


/****************************************************/
double tanDeg(double deg)
/****************************************************/
{
	double rad = deg * (PI/180);
	return (tan(rad));
}

/****************************************************/
double toDeg(double rad)
/****************************************************/
{
	return ((rad*180) / PI);
}

/****************************************************/
double toRad(double deg)
/****************************************************/
{
	return (deg * (PI/180));
}
